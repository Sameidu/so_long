#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int	len;
	int	i;
	int	j;

	if (!s1 || !set)
		return (0);
	len = ft_strlen(s1);
	i = 0;
	j = len;
	while (i <= len && ft_strchr(set, s1[i]) != NULL)
		i++;
	while (j >= i && ft_strchr(set, s1[j]) != NULL)
		j--;
	if (i > j)
		return (ft_strdup(""));
	len = j - i;
	return (ft_substr(s1, i, len + 1));
}
